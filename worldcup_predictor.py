import numpy as np

def make_long_format(data):
    home = data[['match_id','date','home_team','away_team','home_score','away_score','neutral']].copy()
    home.columns = ['match_id','date','team','opponent','goals_for','goals_against','neutral']
    home['is_home'] = 1

    away = data[['match_id','date','away_team','home_team','away_score','home_score','neutral']].copy()
    away.columns = ['match_id','date','team','opponent','goals_for','goals_against','neutral']
    away['is_home'] = 0

    long_data = pd.concat([home, away], ignore_index=True)
    long_data = long_data.sort_values(['team','date']).reset_index(drop=True)

    long_data['result'] = np.where(long_data['goals_for'] > long_data['goals_against'], 'W',
                            np.where(long_data['goals_for'] < long_data['goals_against'], 'L', 'D'))
    long_data['points'] = long_data['result'].map({'W':3,'D':1,'L':0})
    return long_data

