def add_head_to_head(data):
    data = data.sort_values('date').reset_index(drop=True)
    h2h_record = {}
    winrates, match_counts = [], []

    for idx, row in data.iterrows():
        key = tuple(sorted([row['home_team'], row['away_team']]))
        history = h2h_record.get(key, [])
        match_counts.append(len(history))

        if len(history) > 0:
            home_wins = sum(1 for w in history if w == row['home_team'])
            winrates.append(home_wins / len(history))
        else:
            winrates.append(0.5)

        if row['home_score'] > row['away_score']:
            winner = row['home_team']
        elif row['away_score'] > row['home_score']:
            winner = row['away_team']
        else:
            winner = 'Draw'
        history.append(winner)
        h2h_record[key] = history

    data['h2h_matches'] = match_counts
    data['h2h_home_winrate'] = winrates
    return data

